-module(connack).

%% API
-export([get_pkt/1]).

%%%===================================================================
%%% API
%%%===================================================================
-type session_present() :: 0..1.
-type return_code() :: 0..5.
-spec get_pkt({session_present(), return_code()}) ->
	   <<_:32>>.
%%--------------------------------------------------------------------
%% @doc
%% @spec
%% @end
%%--------------------------------------------------------------------
get_pkt({SessionPresent, ReturnCode}) when ReturnCode >= 0, ReturnCode =< 5 ->
    <<2:4, 0:4, 2:8, 0:7, SessionPresent:1, ReturnCode:8>>.
